package entidades;

public class TIpoDeQuarto {
    private float valor;
    private String descricao;

    public void setValor(float valor) {
        this.valor = valor;
    }

    public float getValor() {
        return valor;
    } 

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getDescricao() {
        return descricao;
    }
}
